public class Komentar {
    public static void main(String[] args) {
        System.out.println(sum(10,10));
    }

    /** -> Multi Line -> klik /** + Enter
     * Method untuk menjumlahkan value1 dengan value2
     * @param value1 angka pertama
     * @param value2 angka kedua
     * @return hasil penjumlahan value2 dengan value2
     */

    static int sum(int value1, int value2) {
//      Jumlahkan value1 dengan value2 -> single line -> klik CTRL + Enter
        return value1 + value2;
    }
}
